void main() {
  var estacao = 'Salida';
  switch (estacao) {
    case 'Outono':
      print('Temperatura amena');
      break;

    case 'Verão':
      print('Ta calor');
      break;

    case 'Inverno':
      print('Ta frio ');
      break;

    case 'Primavera':
      print('Temporada das flores');
      break;

    default:
      print('Tá bom..');
  }
}
